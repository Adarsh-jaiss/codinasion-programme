// Write a programme to subtract two numbers
#include <iostream>

int main()
{
    // declare variables
    int x, y;

    // get input
    std::cout << "Enter two numbers ";
    std::cin >> x;
    std::cin >> y;

    // print difference
    std::cout << "Difference of numbers : " << (x - y) << std::endl;

    return 0;
}
